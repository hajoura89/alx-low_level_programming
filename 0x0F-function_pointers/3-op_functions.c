#include "3-calc.h"

/**
 * op_add - calculates the sum of two params
 * @a: first param
 * @b: second param
 * Return: sum result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the difference of two params
 * @a: first param
 * @b: second param
 * Return: difference result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the product of two params
 * @a: first param
 * @b: second param
 * Return: product result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the division of two params
 * @a: first param
 * @b: second param
 * Return: result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the remainder of the division of two params
 * @a: first param
 * @b: second param
 * Return: remainder of the division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
